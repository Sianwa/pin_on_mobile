package com.interswitchgroup.pinonmobile.models;

public class Keys {

    private String mleKey;
    private String sessionKey;
    private String sessionKeyId;

    public Keys() {
    }

    public String getMleKey() {
        return mleKey;
    }

    public void setMleKey(String mleKey) {
        this.mleKey = mleKey;
    }

    public String getSessionKey() {
        return sessionKey;
    }

    public void setSessionKey(String sessionKey) {
        this.sessionKey = sessionKey;
    }

    public String getSessionKeyId() {
        return sessionKeyId;
    }

    public void setSessionKeyId(String sessionKeyId) {
        this.sessionKeyId = sessionKeyId;
    }
}
